inline uint32_t Sobol32(
    uint32_t seqNo,
    const uint32_t *matrix,
    uint32_t scramble = 0
) {                                                                             // Same as PBRT's sobol function, but return an integer
    for (int i = 0; seqNo; i++, seqNo >>= 1) {
        if (seqNo & 1) scramble ^= matrix[i];
    }
    return scramble;
}

